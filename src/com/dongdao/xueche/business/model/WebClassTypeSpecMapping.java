package com.dongdao.xueche.business.model;

public class WebClassTypeSpecMapping {
    private Integer id;

    private Integer classId;

    private Integer classSpecId;

    private Integer sortNo;

    private Integer type;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getClassId() {
        return classId;
    }

    public void setClassId(Integer classId) {
        this.classId = classId;
    }

    public Integer getClassSpecId() {
        return classSpecId;
    }

    public void setClassSpecId(Integer classSpecId) {
        this.classSpecId = classSpecId;
    }

    public Integer getSortNo() {
        return sortNo;
    }

    public void setSortNo(Integer sortNo) {
        this.sortNo = sortNo;
    }

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }
}